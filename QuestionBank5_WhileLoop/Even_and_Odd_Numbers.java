package QuestionBank5_WhileLoop;

public class Even_and_Odd_Numbers {
	public static void main(String[] args) {
		int a=0;
		while(a<=50) {
			System.out.println(a);
			a+=2;
		}
		System.out.println("-------Odd Numbers------");
		
		int b=1;
		while(b<=50) {
		System.out.println(b);
		b+=2;
	}
	}	

}
