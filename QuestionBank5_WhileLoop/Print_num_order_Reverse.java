package QuestionBank5_WhileLoop;

public class Print_num_order_Reverse {
	public static void main(String[] args) {
		int a=1;
		while(a<=10) {
			System.out.println(a);
			a++;
		}System.out.println("-----Reverse method-----");
	
	int b=10;
	while(b>=1) {
		System.out.println(b);
		b--;
	}
		
		}
}
