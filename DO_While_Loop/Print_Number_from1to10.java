package DO_While_Loop;

public class Print_Number_from1to10 {
	public static void main(String[] args) {
		int a=1;
		do {
			System.out.println(a);
			a++;
			
			
		}
		while(a<=10) ;
		System.out.println("**********Reverse Order******");
	
  //Reverse Order
	int b=10;
	do {
		System.out.println(b);
		b--;
		
		
	}
	while(b>=1) ;
}
	
}
